using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _0._05_Loops
{
    class Program
    {
        static void Main(string[] args)
        {
            //For Loop
            for (int i = 0; i < 15; i = i + 2)
            {
                Console.WriteLine(i);
            }

            Console.ReadLine();
        }
    }
}
